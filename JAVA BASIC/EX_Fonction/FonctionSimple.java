/*
    1. Fonction simple
    ------------------
    Écris une fonction `bonjour()` qui affiche "Bonjour le monde !"
    Appelle cette fonction depuis la méthode `main`.
 */
public class FonctionSimple {
    public static void main(String[] args) {
        bonjour();
    }
    public static void bonjour(){
        System.out.println("Bonjour le monde !");
    }
}
